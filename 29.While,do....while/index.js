/*
    while , do .... while loops
    Syntax 

    while (condition) {
        //do something
    }
*/

for (var i = 0 ; i <= 10 ; i++ ) {
    console.log(i);
}

console.log('===========================================');

var i = 1;

while (i <= 10){
    console.log(i);
    ++i;
}

console.log('===========================================');


/*
    Syntax
    do {
        //do something
    }while()
*/

var i = 1;

do {
    console.log(i);
    ++i;
}while(i <= 10)

